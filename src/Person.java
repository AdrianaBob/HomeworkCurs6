public class Person {
    private String name;
    private int age;

    private boolean married;


    public Person() {

    }

    public String getName() {

        return name;
    }

    public void setName(String newName) {

        this.name = newName;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int newAge) {

        this.age = newAge;
    }

    public boolean getMarried() {
        return married;
    }

    public void setMarried(boolean married1) {
        married = true;
    }

}

